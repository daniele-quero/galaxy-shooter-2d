
public class SpawnLimit
{
    private float _yMin;
    private float _yMax;
    private float _xMin;
    private float _xMax;

    public float YMin { get => _yMin; set => _yMin = value; }
    public float XMin { get => _xMin; set => _xMin = value; }
    public float XMax { get => _xMax; set => _xMax = value; }
    public float YMax { get => _yMax; set => _yMax = value; }
}
